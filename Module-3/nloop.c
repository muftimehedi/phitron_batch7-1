#include<stdio.h>
int main()
{
    for(int i = 1; i <= 5; i++)    // বাইরের লুপ: ৫ বার চালাবে
    {
        for(int j = 1; j <= 10; j++)   // ভিতরের লুপ: ১ থেকে ১০ পর্যন্ত প্রিন্ট করবে
        {
            printf("%d ", j);     // সংখ্যাগুলো প্রিন্ট করবে
        }
        printf("\n");             // প্রতি লাইন শেষে নতুন লাইন
    }
    return 0;
}
